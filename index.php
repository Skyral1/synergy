				sciences fortes du campus Sint-Pieters de Gand commenceront à se
				documenter sur le système aquaponique pour pouvoir l'expliquer aux
				élèves des autres écoles.
			</p>

			<p>
				Les élèves de Don Bosco Halle et Don Bosco Remouchamps, ayant reçu les
